package clientgenv2

import (
	"bytes"
	_ "embed" // used to load template file
	"fmt"
	"go/types"
	"strings"

	"github.com/99designs/gqlgen/codegen/config"
	"github.com/99designs/gqlgen/codegen/templates"

	gqlgencConfig "github.com/gqlgo/gqlgenc/config"
)

//go:embed template.gotpl
var template string

func RenderTemplate(cfg *config.Config, fragments []*Fragment, operations []*Operation, operationResponses []*OperationResponse, structSources []*StructSource, generateCfg *gqlgencConfig.GenerateConfig, client config.PackageConfig) error {
	genGettersGenerator := &GenGettersGenerator{
		ClientPackageName: client.Package,
	}

	err := templates.Render(templates.Options{
		PackageName: client.Package,
		Filename:    client.Filename,
		Template:    template,
		Data: map[string]any{
			"Fragment":            fragments,
			"Operation":           operations,
			"OperationResponse":   operationResponses,
			"GenerateClient":      generateCfg.ShouldGenerateClient(),
			"StructSources":       structSources,
			"ClientInterfaceName": generateCfg.GetClientInterfaceName(),
		},
		Packages:   cfg.Packages,
		PackageDoc: "// Code generated by github.com/gqlgo/gqlgenc, DO NOT EDIT.\n",
		Funcs: map[string]any{
			"genGetters":             genGettersGenerator.GenFunc(),
			"genConversionGetters": genGettersGenerator.ConversionGettersFunc(fragments),
		},
	})
	if err != nil {
		return fmt.Errorf("%s generating failed: %w", client.Filename, err)
	}

	return nil
}

type GenGettersGenerator struct {
	ClientPackageName string
}

func (g *GenGettersGenerator) GenFunc() func(name string, p types.Type) string {
	// This method returns a string of getters for a struct.
	// The idea is to be able to chain calls safely without having to check for nil.
	// To make this work we need to return a pointer to the struct if the field is a struct.
	return func(name string, p types.Type) string {
		var it *types.Struct

		it, ok := p.(*types.Struct)
		if !ok {
			return ""
		}

		var buf bytes.Buffer

		for i := range it.NumFields() {
			field := it.Field(i)

			returns := g.returnTypeName(field.Type(), false)

			buf.WriteString("func (t *" + name + ") Get" + field.Name() + "() " + returns + "{\n")
			buf.WriteString("if t == nil {\n t = &" + name + "{}\n}\n")

			pointerOrNot := ""
			if _, ok := field.Type().(*types.Named); ok {
				pointerOrNot = "&"
			}

			buf.WriteString("return " + pointerOrNot + "t." + field.Name() + "\n}\n")
		}

		return buf.String()
	}
}

func (g *GenGettersGenerator) returnTypeName(t types.Type, nested bool) string {
	switch it := t.(type) {
	case *types.Basic:
		return it.String()
	case *types.Pointer:
		return "*" + g.returnTypeName(it.Elem(), true)
	case *types.Slice:
		return "[]" + g.returnTypeName(it.Elem(), true)
	case *types.Named:
		s := strings.Split(it.String(), ".")
		name := s[len(s)-1]

		isImported := it.Obj().Parent() != nil && it.Obj().Pkg().Name() != g.ClientPackageName
		if isImported {
			name = namedTypeString(it)
		}

		if nested {
			return name
		}

		return "*" + name
	case *types.Interface:
		return "any"
	case *types.Map:
		return "map[" + g.returnTypeName(it.Key(), true) + "]" + g.returnTypeName(it.Elem(), true)
	case *types.Alias:
		return g.returnTypeName(it.Underlying(), nested)
	default:
		return fmt.Sprintf("%T----", it)
	}
}

// ConversionGettersFunc returns a template function that generates conversion getters
// for spread fragments. Each getter constructs the original fragment type from the
// flattened parent struct's fields.
func (g *GenGettersGenerator) ConversionGettersFunc(allFragments []*Fragment) func(ownerName string, spreads []*SpreadFragmentInfo) string {
	fragmentMap := make(map[string]*Fragment, len(allFragments))
	for _, f := range allFragments {
		fragmentMap[f.Name] = f
	}

	return func(ownerName string, spreads []*SpreadFragmentInfo) string {
		if len(spreads) == 0 {
			return ""
		}

		var buf bytes.Buffer

		for _, spread := range spreads {
			targetStruct := resolveTargetFragment(fragmentMap, spread)
			if targetStruct == nil {
				continue
			}

			g.writeConversionGetter(&buf, ownerName, g.typeName(spread.Type), targetStruct)
		}

		return buf.String()
	}
}

// resolveTargetFragment looks up a spread fragment's definition and returns its
// underlying struct type, or nil if not found.
func resolveTargetFragment(fragmentMap map[string]*Fragment, spread *SpreadFragmentInfo) *types.Struct {
	targetFragment, ok := fragmentMap[spread.Name]
	if !ok {
		return nil
	}

	targetStruct, ok := targetFragment.Type.(*types.Struct)
	if !ok {
		return nil
	}

	return targetStruct
}

// writeConversionGetter writes a single conversion getter method that constructs
// the target fragment type from the owner struct's fields.
func (g *GenGettersGenerator) writeConversionGetter(buf *bytes.Buffer, ownerName string, targetTypeName string, targetStruct *types.Struct) {
	buf.WriteString("func (t *" + ownerName + ") Get" + targetTypeName + "() *" + targetTypeName + " {\n")
	buf.WriteString("if t == nil {\n t = &" + ownerName + "{}\n}\n")
	buf.WriteString("return &" + targetTypeName + "{\n")
	g.writeStructLiteral(buf, targetStruct, "t")
	buf.WriteString("}\n}\n")
}

// writeStructLiteral writes field assignments for a struct literal, delegating
// each field to writeFieldAssignment.
func (g *GenGettersGenerator) writeStructLiteral(buf *bytes.Buffer, targetStruct *types.Struct, sourceExpr string) {
	for i := range targetStruct.NumFields() {
		g.writeFieldAssignment(buf, targetStruct.Field(i), sourceExpr)
	}
}

// writeFieldAssignment writes a single field assignment in a struct literal.
// For named struct types, it recursively constructs a nested struct literal.
// For other types, it directly assigns from the source expression.
func (g *GenGettersGenerator) writeFieldAssignment(buf *bytes.Buffer, field *types.Var, sourceExpr string) {
	fieldName := field.Name()

	nestedStruct := underlyingStruct(field.Type())
	if nestedStruct != nil {
		buf.WriteString(fieldName + ": " + g.typeName(field.Type()) + "{\n")
		g.writeStructLiteral(buf, nestedStruct, sourceExpr+"."+fieldName)
		buf.WriteString("},\n")
	} else {
		buf.WriteString(fieldName + ": " + sourceExpr + "." + fieldName + ",\n")
	}
}

// typeName extracts the short type name from a types.Type.
func (g *GenGettersGenerator) typeName(t types.Type) string {
	switch it := t.(type) {
	case *types.Named:
		s := strings.Split(it.String(), ".")
		return s[len(s)-1]
	case *types.Pointer:
		return g.typeName(it.Elem())
	default:
		return t.String()
	}
}

// underlyingStruct returns the *types.Struct if t is a named struct type, nil otherwise.
func underlyingStruct(t types.Type) *types.Struct {
	// Unwrap pointer
	if ptr, ok := t.(*types.Pointer); ok {
		t = ptr.Elem()
	}

	named, ok := t.(*types.Named)
	if !ok {
		return nil
	}

	st, ok := named.Underlying().(*types.Struct)
	if !ok {
		return nil
	}

	return st
}

func namedTypeString(named *types.Named) string {
	// オブジェクトからパッケージ情報を取得
	pkg := named.Obj().Pkg()

	// パッケージ情報がない場合、型名のみを返す
	if pkg == nil {
		return named.Obj().Name()
	}

	// パッケージ名と型名を結合して返す
	return fmt.Sprintf("%s.%s", pkg.Name(), named.Obj().Name())
}
